CREATE OR REPLACE FUNCTION validate_attendance_event_insert()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
  session_record public.attendance_sessions%ROWTYPE;
  account_record public.users%ROWTYPE;
BEGIN
  SELECT *
  INTO session_record
  FROM public.attendance_sessions
  WHERE id = NEW.session_id;

  IF NOT FOUND OR session_record.user_id IS DISTINCT FROM NEW.user_id THEN
    RAISE EXCEPTION 'Attendance event must belong to its session owner.';
  END IF;

  IF (session_record.session_type = 'stationary_day'
      AND NEW.event_type NOT IN ('time_in', 'lunch_out', 'lunch_in', 'time_out', 'gps_ping'))
    OR (session_record.session_type = 'field_visit'
      AND NEW.event_type NOT IN ('visit_in', 'visit_out', 'gps_ping'))
  THEN
    RAISE EXCEPTION 'Attendance event type is incompatible with the session type.';
  END IF;

  SELECT *
  INTO account_record
  FROM public.users
  WHERE id = NEW.user_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Attendance event user must exist.';
  END IF;

  IF account_record.active = false
    AND (
      account_record.deactivated_at IS NULL
      OR NEW.captured_at_local > account_record.deactivated_at
    )
  THEN
    RAISE EXCEPTION 'Attendance captured after account deactivation is not accepted.';
  END IF;

  NEW.received_at_server = now();
  NEW.gps_expires_at = NEW.captured_at_local + interval '12 months';

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION snapshot_attendance_flag_workflow()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
  flag_work_date date;
  selected_workflow text;
  selected_effective_from date;
BEGIN
  SELECT work_date
  INTO flag_work_date
  FROM public.attendance_sessions
  WHERE id = NEW.session_id
    AND user_id = NEW.user_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Attendance flag must belong to its session owner.';
  END IF;

  IF NEW.attendance_event_id IS NOT NULL
    AND NOT EXISTS (
      SELECT 1
      FROM public.attendance_events
      WHERE id = NEW.attendance_event_id
        AND session_id = NEW.session_id
        AND user_id = NEW.user_id
    )
  THEN
    RAISE EXCEPTION 'Attendance flag event must belong to the same user and session.';
  END IF;

  SELECT
    rules.rule_value ->> NEW.flag_type::text,
    rules.effective_from
  INTO selected_workflow, selected_effective_from
  FROM public.attendance_rules AS rules
  WHERE rules.rule_key = 'flag_review_workflow_mode_by_flag_type'
    AND rules.effective_from <= flag_work_date
    AND (
      rules.effective_to IS NULL
      OR rules.effective_to >= flag_work_date
    )
  ORDER BY rules.effective_from DESC
  LIMIT 1;

  IF selected_workflow IS NULL
    OR selected_workflow NOT IN (
      'manager_review_admin_observe',
      'manager_preapprove_admin_final',
      'manager_view_admin_approve'
    )
  THEN
    RAISE EXCEPTION 'No valid flag review workflow is configured for flag type % on work date %.', NEW.flag_type, flag_work_date;
  END IF;

  NEW.workflow_mode = selected_workflow::flag_review_workflow_mode;
  NEW.workflow_effective_from = selected_effective_from;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION create_automatic_attendance_flags()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
  account_record public.users%ROWTYPE;
  gps_threshold numeric;
  clock_threshold_minutes numeric;
BEGIN
  SELECT *
  INTO account_record
  FROM public.users
  WHERE id = NEW.user_id;

  IF account_record.active = false
    AND account_record.deactivated_at IS NOT NULL
    AND NEW.captured_at_local <= account_record.deactivated_at
  THEN
    INSERT INTO public.attendance_flags (
      session_id,
      attendance_event_id,
      user_id,
      flag_type,
      severity,
      evidence,
      workflow_mode,
      workflow_effective_from
    ) VALUES (
      NEW.session_id,
      NEW.id,
      NEW.user_id,
      'deactivated_user_record',
      'high',
      jsonb_build_object('deactivated_at', account_record.deactivated_at, 'captured_at_local', NEW.captured_at_local),
      'manager_review_admin_observe',
      CURRENT_DATE
    );
  END IF;

  IF NEW.event_type IN ('time_in', 'time_out', 'visit_in', 'visit_out')
    AND NEW.photo_path IS NULL
  THEN
    INSERT INTO public.attendance_flags (
      session_id,
      attendance_event_id,
      user_id,
      flag_type,
      severity,
      evidence,
      workflow_mode,
      workflow_effective_from
    ) VALUES (
      NEW.session_id,
      NEW.id,
      NEW.user_id,
      'missing_photo',
      'warning',
      jsonb_build_object('event_type', NEW.event_type),
      'manager_review_admin_observe',
      CURRENT_DATE
    );
  END IF;

  IF NEW.offline_declared THEN
    INSERT INTO public.attendance_flags (
      session_id,
      attendance_event_id,
      user_id,
      flag_type,
      severity,
      evidence,
      workflow_mode,
      workflow_effective_from
    ) VALUES (
      NEW.session_id,
      NEW.id,
      NEW.user_id,
      'offline_submission',
      'warning',
      NEW.offline_evidence || jsonb_build_object('received_at_server', NEW.received_at_server),
      'manager_view_admin_approve',
      CURRENT_DATE
    );

    IF NEW.received_at_server - NEW.captured_at_local > interval '24 hours' THEN
      INSERT INTO public.attendance_flags (
        session_id,
        attendance_event_id,
        user_id,
        flag_type,
        severity,
        evidence,
        workflow_mode,
        workflow_effective_from
      ) VALUES (
        NEW.session_id,
        NEW.id,
        NEW.user_id,
        'late_sync',
        'warning',
        jsonb_build_object(
          'delay_seconds', EXTRACT(EPOCH FROM NEW.received_at_server - NEW.captured_at_local),
          'received_at_server', NEW.received_at_server
        ),
        'manager_review_admin_observe',
        CURRENT_DATE
      );
    END IF;
  END IF;

  SELECT (rules.rule_value #>> '{}')::numeric
  INTO gps_threshold
  FROM public.attendance_rules AS rules
  JOIN public.attendance_sessions AS sessions
    ON sessions.id = NEW.session_id
  WHERE rules.rule_key = 'gps_low_accuracy_threshold_meters'
    AND rules.effective_from <= sessions.work_date
    AND (
      rules.effective_to IS NULL
      OR rules.effective_to >= sessions.work_date
    )
  ORDER BY rules.effective_from DESC
  LIMIT 1;

  IF NEW.gps_accuracy_meters IS NOT NULL
    AND gps_threshold IS NOT NULL
    AND NEW.gps_accuracy_meters > gps_threshold
  THEN
    INSERT INTO public.attendance_flags (
      session_id,
      attendance_event_id,
      user_id,
      flag_type,
      severity,
      evidence,
      workflow_mode,
      workflow_effective_from
    ) VALUES (
      NEW.session_id,
      NEW.id,
      NEW.user_id,
      'gps_low_accuracy',
      'warning',
      jsonb_build_object('gps_accuracy_meters', NEW.gps_accuracy_meters, 'threshold_meters', gps_threshold),
      'manager_review_admin_observe',
      CURRENT_DATE
    );
  END IF;

  SELECT (rules.rule_value #>> '{}')::numeric
  INTO clock_threshold_minutes
  FROM public.attendance_rules AS rules
  JOIN public.attendance_sessions AS sessions
    ON sessions.id = NEW.session_id
  WHERE rules.rule_key = 'clock_discrepancy_threshold_minutes'
    AND rules.effective_from <= sessions.work_date
    AND (
      rules.effective_to IS NULL
      OR rules.effective_to >= sessions.work_date
    )
  ORDER BY rules.effective_from DESC
  LIMIT 1;

  IF NEW.offline_declared = false
    AND clock_threshold_minutes IS NOT NULL
    AND abs(EXTRACT(EPOCH FROM NEW.received_at_server - NEW.captured_at_local))
      > clock_threshold_minutes * 60
  THEN
    INSERT INTO public.attendance_flags (
      session_id,
      attendance_event_id,
      user_id,
      flag_type,
      severity,
      evidence,
      workflow_mode,
      workflow_effective_from
    ) VALUES (
      NEW.session_id,
      NEW.id,
      NEW.user_id,
      'clock_discrepancy',
      'high',
      jsonb_build_object(
        'delta_seconds', abs(EXTRACT(EPOCH FROM NEW.received_at_server - NEW.captured_at_local)),
        'threshold_minutes', clock_threshold_minutes
      ),
      'manager_review_admin_observe',
      CURRENT_DATE
    );
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION validate_attendance_flag_review_insert()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
DECLARE
  flag_record public.attendance_flags%ROWTYPE;
  actor_role public.user_role;
  has_manager_access boolean;
BEGIN
  SELECT *
  INTO flag_record
  FROM public.attendance_flags
  WHERE id = NEW.attendance_flag_id;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Attendance flag review requires an existing attendance flag.';
  END IF;

  SELECT role
  INTO actor_role
  FROM public.users
  WHERE id = NEW.actor_user_id
    AND active = true;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Attendance flag reviewer must be an active user.';
  END IF;

  IF auth.uid() IS NOT NULL AND NEW.actor_user_id IS DISTINCT FROM auth.uid() THEN
    RAISE EXCEPTION 'Attendance flag reviewer must match the current user.';
  END IF;

  IF NEW.stage = 'manager' THEN
    SELECT EXISTS (
      SELECT 1
      FROM public.manager_staff_assignments AS assignment
      WHERE assignment.manager_id = NEW.actor_user_id
        AND assignment.staff_user_id = flag_record.user_id
        AND assignment.effective_from <= CURRENT_DATE
        AND (
          assignment.effective_to IS NULL
          OR assignment.effective_to >= CURRENT_DATE
        )
      UNION ALL
      SELECT 1
      FROM public.manager_delegations AS delegation
      JOIN public.manager_delegation_capabilities AS capability
        ON capability.manager_delegation_id = delegation.id
       AND capability.capability = 'review_flags'
      JOIN public.manager_staff_assignments AS assignment
        ON assignment.manager_id = delegation.original_manager_id
       AND assignment.staff_user_id = flag_record.user_id
      WHERE delegation.covering_manager_id = NEW.actor_user_id
        AND delegation.revoked_at IS NULL
        AND delegation.effective_from <= CURRENT_DATE
        AND delegation.effective_to >= CURRENT_DATE
        AND assignment.effective_from <= CURRENT_DATE
        AND (
          assignment.effective_to IS NULL
          OR assignment.effective_to >= CURRENT_DATE
        )
    ) INTO has_manager_access;

    IF actor_role <> 'manager' OR NOT has_manager_access THEN
      RAISE EXCEPTION 'Manager flag review requires active review access to the affected staff member.';
    END IF;

    IF (flag_record.workflow_mode = 'manager_review_admin_observe'
        AND NEW.decision NOT IN ('approved', 'rejected', 'resolved'))
      OR (flag_record.workflow_mode = 'manager_preapprove_admin_final'
        AND NEW.decision NOT IN ('pre_approved', 'rejected'))
      OR flag_record.workflow_mode = 'manager_view_admin_approve'
    THEN
      RAISE EXCEPTION 'Manager review decision is incompatible with the flag workflow.';
    END IF;
  ELSE
    IF actor_role <> 'admin' THEN
      RAISE EXCEPTION 'Admin flag review requires an active admin.';
    END IF;

    IF flag_record.workflow_mode = 'manager_review_admin_observe'
      OR NEW.decision NOT IN ('approved', 'rejected', 'resolved')
    THEN
      RAISE EXCEPTION 'Admin review decision is incompatible with the flag workflow.';
    END IF;

    IF flag_record.workflow_mode = 'manager_preapprove_admin_final'
      AND NOT EXISTS (
        SELECT 1
        FROM public.attendance_flag_reviews
        WHERE attendance_flag_id = flag_record.id
          AND stage = 'manager'
          AND decision = 'pre_approved'
      )
    THEN
      RAISE EXCEPTION 'Admin final review requires an earlier manager pre-approval.';
    END IF;
  END IF;

  RETURN NEW;
END;
$$;

