CREATE POLICY "Users can select own attendance events"
ON attendance_events
FOR SELECT
TO authenticated
USING (user_id = auth.uid());

CREATE POLICY "Managers can select direct or delegated team attendance events"
ON attendance_events
FOR SELECT
TO authenticated
USING (
  current_user_role() = 'manager'
  AND (
    EXISTS (
      SELECT 1
      FROM manager_staff_assignments
      WHERE manager_id = auth.uid()
        AND staff_user_id = attendance_events.user_id
        AND effective_from <= CURRENT_DATE
        AND (effective_to IS NULL OR effective_to >= CURRENT_DATE)
    )
    OR has_active_manager_delegation_for_staff(attendance_events.user_id, 'view_attendance')
    OR has_active_manager_delegation_for_staff(attendance_events.user_id, 'review_flags')
  )
);

CREATE POLICY "Admins can select all attendance events"
ON attendance_events
FOR SELECT
TO authenticated
USING (current_user_role() = 'admin');

