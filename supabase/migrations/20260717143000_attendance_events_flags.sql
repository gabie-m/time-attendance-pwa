ON attendance_flag_reviews
FOR SELECT
TO authenticated
USING (
  current_user_role() = 'manager'
  AND EXISTS (
    SELECT 1
    FROM attendance_flags
    WHERE attendance_flags.id = attendance_flag_reviews.attendance_flag_id
      AND (
        EXISTS (
          SELECT 1
          FROM manager_staff_assignments
          WHERE manager_id = auth.uid()
            AND staff_user_id = attendance_flags.user_id
            AND effective_from <= CURRENT_DATE
            AND (effective_to IS NULL OR effective_to >= CURRENT_DATE)
        )
        OR has_active_manager_delegation_for_staff(attendance_flags.user_id, 'view_attendance')
        OR has_active_manager_delegation_for_staff(attendance_flags.user_id, 'review_flags')
      )
  )
