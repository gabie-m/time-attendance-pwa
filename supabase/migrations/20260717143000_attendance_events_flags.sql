  'manager_preapprove_admin_final',
  'manager_view_admin_approve'
);
CREATE TYPE flag_review_stage AS ENUM ('manager', 'admin');
CREATE TYPE flag_review_decision AS ENUM (
  'approved',
  'pre_approved',
  'rejected',
  'resolved'
);

