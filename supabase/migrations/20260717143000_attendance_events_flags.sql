-- Direct authenticated session writes are already revoked. Keep the session
-- guard compatible with offline events captured before a later deactivation;
-- the event insert trigger below is the time-aware rejection point.
CREATE OR REPLACE FUNCTION require_active_consented_attendance_session_user()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = pg_catalog, public
AS $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM public.users
    WHERE id = NEW.user_id
      AND location_consent_given_at IS NOT NULL
      AND (active = true OR deactivated_at IS NOT NULL)
  ) THEN
    RAISE EXCEPTION 'Attendance sessions require a consented active account or a deactivated account with a recorded deactivation time.';
  END IF;

  RETURN NEW;
END;
$$;

CREATE TABLE attendance_events (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  session_id uuid NOT NULL,
  user_id uuid NOT NULL,
  client_event_id uuid NOT NULL,
  event_type event_type NOT NULL,
  captured_at_local timestamptz NOT NULL,
  received_at_server timestamptz NOT NULL DEFAULT now(),
  location_id uuid,
  latitude numeric(9, 6),
  longitude numeric(9, 6),
  gps_accuracy_meters numeric(10, 2),
  gps_expires_at timestamptz NOT NULL,
  offline_declared boolean NOT NULL DEFAULT false,
  offline_evidence jsonb NOT NULL DEFAULT '{}'::jsonb,
  photo_path text,
  photo_metadata jsonb NOT NULL DEFAULT '{}'::jsonb,
  photo_verified boolean NOT NULL DEFAULT false,
  validation_status validation_status NOT NULL DEFAULT 'normal',
  validation_evidence jsonb NOT NULL DEFAULT '{}'::jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),

  CONSTRAINT attendance_events_session_id_fkey
    FOREIGN KEY (session_id) REFERENCES attendance_sessions(id),
  CONSTRAINT attendance_events_user_id_fkey
    FOREIGN KEY (user_id) REFERENCES users(id),
  CONSTRAINT attendance_events_location_id_fkey
    FOREIGN KEY (location_id) REFERENCES locations(id),
  CONSTRAINT attendance_events_user_client_event_unique
    UNIQUE (user_id, client_event_id),
  CONSTRAINT attendance_events_coordinates_pair_check CHECK (
    (latitude IS NULL AND longitude IS NULL)
    OR (latitude IS NOT NULL AND longitude IS NOT NULL)
  ),
  CONSTRAINT attendance_events_latitude_valid CHECK (
    latitude IS NULL OR latitude BETWEEN -90 AND 90
  ),
  CONSTRAINT attendance_events_longitude_valid CHECK (
    longitude IS NULL OR longitude BETWEEN -180 AND 180
  ),
  CONSTRAINT attendance_events_gps_accuracy_non_negative CHECK (
    gps_accuracy_meters IS NULL OR gps_accuracy_meters >= 0
  ),
  CONSTRAINT attendance_events_private_photo_path_check CHECK (
    photo_path IS NULL OR photo_path !~* '^https?://'
  ),
  CONSTRAINT attendance_events_offline_evidence_object_check CHECK (
    jsonb_typeof(offline_evidence) = 'object'
  ),
  CONSTRAINT attendance_events_photo_metadata_object_check CHECK (
    jsonb_typeof(photo_metadata) = 'object'
  ),
  CONSTRAINT attendance_events_validation_evidence_object_check CHECK (
    jsonb_typeof(validation_evidence) = 'object'
  )
);

COMMENT ON TABLE attendance_events IS
  'Append-only attendance evidence. received_at_server is generated by PostgreSQL; client capture time remains evidence. Private photo paths and precise GPS data require retention processing.';
COMMENT ON COLUMN attendance_events.photo_path IS
  'Private Supabase Storage object path only. Never store a public or permanent signed URL.';
COMMENT ON COLUMN attendance_events.offline_declared IS
  'Primary offline classification signal supplied by the client. Receipt delay is supporting evidence only.';

